function resolveKey(
  prefix: string,
  suffixes: Record<string, string>,
  value: string,
): string | null {
  const suffix = suffixes[value];
  return suffix ? `${prefix}.${suffix}` : null;
}

export function getVisualizationActionLabelKey(label: string): string | null {
  return resolveKey('features.algorithms.toolbar.actions', ACTION_KEY_SUFFIXES, label);
}

export function getVisualizationSizeUnitLabelKey(unit: string): string | null {
  return resolveKey('features.algorithms.toolbar.units', UNIT_KEY_SUFFIXES, unit);
}

export function getVisualizationVariantLabelKey(label: string): string | null {
  return resolveKey('features.algorithms.toolbar.variants', VARIANT_KEY_SUFFIXES, label);
}
